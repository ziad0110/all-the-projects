/**
 * Live transaltion script
 * Replaces text content of elements with data-translate attribute
 * Or walks the DOM for specific innerText matches if we want a more aggressive auto-translate.
 * For this project, a DOM-walker that translates complete textNodes is used.
 */

class Translator {
    constructor() {
        this.lang = localStorage.getItem('yemensafe_language') || 'ar';
        this.observer = null;
    }

    setLanguage(lang) {
        if (lang !== 'ar' && lang !== 'en') return;
        this.lang = lang;
        localStorage.setItem('yemensafe_language', lang);

        // Set document properties
        if (lang === 'en') {
            document.documentElement.setAttribute('dir', 'ltr');
            document.documentElement.setAttribute('lang', 'en');
        } else {
            document.documentElement.setAttribute('dir', 'rtl');
            document.documentElement.setAttribute('lang', 'ar');
        }

        this.translatePage();
    }

    translatePage() {
        // Stop observer while mutating to prevent infinite loops
        if (this.observer) this.observer.disconnect();

        this.walkDOM(document.body);
        this.translatePlaceholders();

        // Restart observer
        this.startObserver();
    }

    getTranslation(text) {
        // If target is AR, we assume the original text is already AR, but we need to fetch it from original if we are currently EN.
        // Actually, the easiest way is: 
        // HTML has original AR text. When switching to EN, we save the AR text to data-original-text and swap it.
        // When switching back to AR, we put data-original-text back.

        // This is handled in walkDOM.
        return translations['en'][text.trim()];
    }

    walkDOM(node) {
        if (node.nodeType === 3) { // Text node
            let text = node.nodeValue.trim();
            if (text.length > 0) {
                let parent = node.parentNode;
                if (parent.tagName === 'SCRIPT' || parent.tagName === 'STYLE') return;

                // Save original text if not already saved
                if (!parent.hasAttribute('data-original-text')) {
                    parent.setAttribute('data-original-text', text);
                }

                if (this.lang === 'en') {
                    // Try to translate from original text
                    let original = parent.getAttribute('data-original-text');
                    if (translations['en'][original]) {
                        node.nodeValue = node.nodeValue.replace(original, translations['en'][original]);
                    }
                } else if (this.lang === 'ar') {
                    // Revert to original text
                    let original = parent.getAttribute('data-original-text');
                    if (original && translations['en'][original]) {
                        let engText = translations['en'][original];
                        node.nodeValue = node.nodeValue.replace(engText, original);
                    } else if (original) {
                        node.nodeValue = original;
                    }
                }
            }
        } else if (node.nodeType === 1 && node.nodeName !== "SCRIPT" && node.nodeName !== "STYLE") {
            for (let i = 0; i < node.childNodes.length; i++) {
                this.walkDOM(node.childNodes[i]);
            }
        }
    }

    translatePlaceholders() {
        const inputs = document.querySelectorAll('input[placeholder], textarea[placeholder]');
        inputs.forEach(input => {
            if (!input.hasAttribute('data-original-placeholder')) {
                input.setAttribute('data-original-placeholder', input.getAttribute('placeholder'));
            }

            let original = input.getAttribute('data-original-placeholder');

            if (this.lang === 'en' && translations['en'][original]) {
                input.setAttribute('placeholder', translations['en'][original]);
            } else if (this.lang === 'ar') {
                input.setAttribute('placeholder', original);
            }
        });
    }

    startObserver() {
        // Observer to translate dynamically added elements (like modals, toasts, loaded table rows)
        this.observer = new MutationObserver((mutations) => {
            let shouldTranslate = false;
            for (let mutation of mutations) {
                if (mutation.addedNodes.length > 0) {
                    shouldTranslate = true;
                    break;
                }
            }
            if (shouldTranslate) {
                this.observer.disconnect();
                this.translatePage();
            }
        });

        this.observer.observe(document.body, { childList: true, subtree: true });
    }
}

// Global instance
const i18n = new Translator();

// Initialize on load
document.addEventListener('DOMContentLoaded', () => {
    // Only apply if English is selected, because HTML is already Arabic
    if (i18n.lang === 'en') {
        // document dir and lang are set in app.js or html, but we ensure it here
        document.documentElement.setAttribute('dir', 'ltr');
        document.documentElement.setAttribute('lang', 'en');
        // Initial translation delayed slightly to let dynamic content render
        setTimeout(() => i18n.translatePage(), 100);
    }
});

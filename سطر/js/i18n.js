const I18n = {
    currentLang: 'ar',

    init() {
        const savedLang = localStorage.getItem('satr_lang') || 'ar';
        this.setLang(savedLang);
    },

    setLang(lang) {
        this.currentLang = lang;
        localStorage.setItem('satr_lang', lang);
        document.documentElement.lang = lang;

        if (lang === 'ar') {
            document.body.setAttribute('dir', 'rtl');
            document.body.style.fontFamily = "'Cairo', sans-serif";
        } else {
            document.body.setAttribute('dir', 'ltr');
            document.body.style.fontFamily = "'Inter', sans-serif";
        }

        this.updatePage();
    },

    toggleLang() {
        this.setLang(this.currentLang === 'ar' ? 'en' : 'ar');
    },

    t(key) {
        return translations[this.currentLang][key] || key;
    },

    getLocalized(obj, field) {
        return obj[`${field}_${this.currentLang}`] || obj[`${field}_ar`] || '';
    },

    updatePage() {
        document.querySelectorAll('[data-i18n]').forEach(el => {
            const key = el.getAttribute('data-i18n');
            if (el.tagName === 'INPUT' || el.tagName === 'TEXTAREA') {
                el.placeholder = this.t(key);
            } else {
                el.textContent = this.t(key);
            }
        });

        // Update lang toggle button text
        const langBtn = document.querySelector('.lang-toggle');
        if (langBtn) {
            langBtn.textContent = this.currentLang === 'ar' ? 'EN' : 'عربي';
        }

        // Dispatch event for other modules
        window.dispatchEvent(new CustomEvent('langChanged', { detail: { lang: this.currentLang } }));
    },

    isRTL() {
        return this.currentLang === 'ar';
    }
};
